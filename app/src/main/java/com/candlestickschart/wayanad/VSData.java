package com.candlestickschart.wayanad;

public class VSData {
    String AcNo;
    String AcName;
    String Booth_ID;
    String PartNo;
    String Booth_name;
    public VSData(String AcNo,
            String AcName,
                  String Booth_ID,
                          String PartNo,
                          String Booth_name) {
        this.AcName = AcName;
        this.AcNo = AcNo;
        this.Booth_ID = Booth_ID;
        this.Booth_name = Booth_name;
        this.PartNo = PartNo;

    }
}
